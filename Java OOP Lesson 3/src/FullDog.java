public class FullDog {
    private String name;
    private int age;

    public FullDog(String dogName, int dogAge) {
        name = dogName;
        age = dogAge;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
